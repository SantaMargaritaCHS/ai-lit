function generateQuizRendering(activity: ActivityConfig, index: number): string {
  return `          {currentActivity === ${index} && (
            <div className="max-w-3xl mx-auto">
              <div className="bg-white rounded-xl shadow-lg p-8">
                <h2 className="text-2xl font-bold text-gray-900 mb-6">${activity.name}</h2>

                {!quizComplete ? (
                  <div>
                    <p className="text-lg font-semibold text-gray-800 mb-4">
                      Question {currentQuestion + 1} of {quizQuestions.length}
                    </p>
                    <p className="text-gray-900 mb-6">
                      {quizQuestions[currentQuestion].question}
                    </p>

                    <div className="space-y-3 mb-6">
                      {quizQuestions[currentQuestion].options.map((option: string, idx: number) => (
                        <button
                          key={idx}
                          onClick={() => handleQuizAnswer(idx)}
                          disabled={selectedAnswer !== null}
                          className={\`w-full p-4 text-left rounded-lg border-2 transition-colors \${
                            selectedAnswer === idx
                              ? idx === quizQuestions[currentQuestion].correctAnswer
                                ? 'border-green-500 bg-green-50 text-green-900'
                                : 'border-red-500 bg-red-50 text-red-900'
                              : selectedAnswer !== null && idx === quizQuestions[currentQuestion].correctAnswer
                              ? 'border-green-500 bg-green-50 text-green-900'
                              : 'border-gray-300 bg-white hover:border-purple-300 hover:bg-purple-50 text-gray-900'
                          }\`}
                        >
                          <div className="flex items-start gap-3">
                            <span className="font-bold">{String.fromCharCode(65 + idx)}.</span>
                            <span>{option}</span>
                          </div>
                        </button>
                      ))}
                    </div>

                    {selectedAnswer !== null && (
                      <div className={\`p-4 rounded-lg \${
                        selectedAnswer === quizQuestions[currentQuestion].correctAnswer
                          ? 'bg-green-50 border border-green-200'
                          : 'bg-red-50 border border-red-200'
                      }\`}>
                        <p className="text-sm font-semibold text-gray-900 mb-2">
                          {selectedAnswer === quizQuestions[currentQuestion].correctAnswer
                            ? '✅ Correct!'
                            : '❌ Incorrect'}
                        </p>
                        <p className="text-sm text-gray-800">
                          {quizQuestions[currentQuestion].explanation}
                        </p>
                      </div>
                    )}

                    {selectedAnswer !== null && (
                      <Button
                        onClick={handleNextQuestion}
                        className="mt-6 bg-purple-600 hover:bg-purple-700 text-white"
                      >
                        {currentQuestion < quizQuestions.length - 1 ? 'Next Question' : 'Complete Quiz'}
                      </Button>
                    )}
                  </div>
                ) : (
                  <div className="text-center">
                    <p className="text-2xl font-bold text-green-600 mb-4">Quiz Complete!</p>
                    <p className="text-gray-700 mb-6">
                      You got {quizScore} out of {quizQuestions.length} correct.
                    </p>
                    <Button
                      onClick={handleActivityComplete}
                      className="bg-purple-600 hover:bg-purple-700 text-white"
                    >
                      Continue to Next Activity
                    </Button>
                  </div>
                )}
              </div>
            </div>
          )}`;
}

