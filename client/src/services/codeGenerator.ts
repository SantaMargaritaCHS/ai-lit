/**
 * Module definition structure (from ModuleAssembly)
 */
export interface ModuleDefinition {
  id: string;
  title: string;
  description: string;
  targetAudience: string;
  estimatedTime: string;
  activities: ActivityConfig[];
  videos: any[];
  metadata: {
    author: string;
    version: string;
    createdAt: string;
    updatedAt: string;
  };
}

export interface ActivityConfig {
  id: string;
  type: string;
  name: string;
  position: number;
  props: Record<string, any>;
  notes?: string;
}

/**
 * Generate complete module TypeScript file
 */
export function generateCompleteModule(definition: ModuleDefinition): string {
  const moduleName = toPascalCase(definition.id);
  const activityTypes = definition.activities.map((a) => a.type);

  // 1. Generate imports
  const imports = generateImports(activityTypes);

  // 2. Generate module header comment
  const header = generateModuleHeader(definition);

  // 3. Generate interface
  const interfaceCode = generateInterface(moduleName);

  // 4. Generate activity definitions
  const activityDefinitions = generateActivityDefinitions(definition.activities);

  // 5. Generate state declarations
  const stateDeclarations = generateStateDeclarations(definition.activities);

  // 6. Generate activity data
  const activityData = generateActivityData(definition.activities);

  // 7. Generate activity handlers
  const activityHandlers = generateActivityHandlers(definition.activities);

  // 8. Generate activity rendering
  const activityRendering = generateActivityRendering(definition.activities);

  // 9. Assemble complete file
  let code = imports + '\n\n';
  code += header + '\n\n';
  code += interfaceCode + '\n\n';
  code += `const MODULE_ID = '${definition.id}';\n`;
  code += `const MODULE_VERSION = '${definition.metadata.version}';\n\n`;
  code += `export default function ${moduleName}({ userName, onClose }: ${moduleName}Props) {\n`;
  code += '  // State\n';
  code += '  const [currentActivity, setCurrentActivity] = useState(0);\n';
  code += stateDeclarations + '\n';
  code += '  const [activities, setActivities] = useState<{ id: string; title: string; completed: boolean }[]>([\n';
  code += activityDefinitions + '\n';
  code += '  ]);\n\n';
  code += '  const [savedProgress, setSavedProgress] = useState<any>(null);\n';
  code += '  const [showResumeDialog, setShowResumeDialog] = useState(false);\n\n';
  code += '  const { registerActivity, clearRegistry } = useActivityRegistry();\n\n';
  code += generateProgressPersistenceLogic();
  code += '\n\n';
  code += generateDevModeIntegration();
  code += '\n\n';
  code += activityData;
  code += '\n\n';
  code += activityHandlers;
  code += '\n\n';
  code += generateStandardHandlers();
  code += '\n\n';
  code += '  return (\n';
  code += '    <>\n';
  code += generateResumeDialog();
  code += '\n';
  code += `      <div className="min-h-screen bg-gradient-to-br from-purple-50 to-blue-100">\n`;
  code += generateHeader(definition.title, definition.description);
  code += '\n';
  code += '        <main className="container mx-auto px-4 py-8">\n';
  code += activityRendering;
  code += '\n\n';
  code += generateCertificate(definition.title);
  code += '\n';
  code += '        </main>\n';
  code += '      </div>\n';
  code += '    </>\n';
  code += '  );\n';
  code += '}\n';

  return code;
}

/**
 * Convert kebab-case to PascalCase
 */
function toPascalCase(str: string): string {
  return str
    .split('-')
    .map((word) => word.charAt(0).toUpperCase() + word.slice(1))
    .join('');
}

/**
 * Generate module header comment
 */
function generateModuleHeader(definition: ModuleDefinition): string {
  const date = new Date().toLocaleDateString();
  return `/**
 * ${toPascalCase(definition.id)} - ${definition.description}
 *
 * **Generated by Module Builder** - ${date}
 *
 * Estimated Time: ${definition.estimatedTime}
 * Target Audience: ${definition.targetAudience}
 * Author: ${definition.metadata.author}
 *
 * Features:
 * - Self-contained module with ${definition.activities.length} activities
 * - Progress persistence (resume on refresh)
 * - Developer Mode integration (Ctrl+Alt+D navigation)
 * - Certificate generation on completion
 * - Accessibility compliant (WCAG 2.1 AA)
 *
 * Module Structure:
${definition.activities.map((a, i) => ` * ${i + 1}. ${a.name} (${a.type})`).join('\n')}
 * ${definition.activities.length + 1}. Certificate of Completion
 */`;
}

/**
 * Generate activity definitions for state initialization
 */
function generateActivityDefinitions(activities: ActivityConfig[]): string {
  return activities
    .map(
      (a) => `    { id: '${a.id}', title: '${a.name}', completed: false },`
    )
    .join('\n');
}

/**
 * Generate state declarations for each activity type
 */
function generateStateDeclarations(activities: ActivityConfig[]): string {
  const declarations: string[] = [];

  if (activities.some((a) => a.type === 'quiz')) {
    declarations.push(
      '  const [currentQuestion, setCurrentQuestion] = useState(0);',
      '  const [selectedAnswer, setSelectedAnswer] = useState<number | null>(null);',
      '  const [quizScore, setQuizScore] = useState(0);',
      '  const [quizComplete, setQuizComplete] = useState(false);'
    );
  }

  if (activities.some((a) => a.type === 'reflection')) {
    declarations.push(
      '  const [reflectionResponse, setReflectionResponse] = useState(\'\');',
      '  const [feedback, setFeedback] = useState(\'\');',
      '  const [showFeedback, setShowFeedback] = useState(false);',
      '  const [needsRetry, setNeedsRetry] = useState(false);',
      '  const [isGeneratingFeedback, setIsGeneratingFeedback] = useState(false);',
      '  const [attemptCount, setAttemptCount] = useState(0);',
      '  const [showEscapeHatch, setShowEscapeHatch] = useState(false);',
      '  const MAX_ATTEMPTS = 2;'
    );
  }

  if (activities.some((a) => a.type === 'scenario')) {
    declarations.push(
      '  const [scenarioResponse, setScenarioResponse] = useState(\'\');'
    );
  }

  if (activities.some((a) => a.type === 'exit-ticket')) {
    declarations.push(
      '  const [selectedPrompt, setSelectedPrompt] = useState<any>(null);',
      '  const [exitTicketResponse, setExitTicketResponse] = useState(\'\');'
    );
  }

  return declarations.join('\n');
}

/**
 * Generate activity data (quiz questions, reflection prompts, etc.)
 */
function generateActivityData(activities: ActivityConfig[]): string {
  let code = '';

  activities.forEach((activity, index) => {
    if (activity.type === 'quiz' && activity.props.questions) {
      code += `  // Quiz questions for Activity ${index + 1}\n`;
      code += `  const quizQuestions = ${JSON.stringify(activity.props.questions, null, 2)};\n\n`;
    }

    if (activity.type === 'exit-ticket' && activity.props.prompts) {
      code += `  // Exit ticket prompts for Activity ${index + 1}\n`;
      code += `  const exitTicketPrompts = ${JSON.stringify(activity.props.prompts, null, 2)};\n\n`;
    }
  });

  return code;
}

/**
 * Generate activity handlers
 */
function generateActivityHandlers(activities: ActivityConfig[]): string {
  let code = '';

  if (activities.some((a) => a.type === 'quiz')) {
    code += `  const handleQuizAnswer = (answerIndex: number) => {
    setSelectedAnswer(answerIndex);
    if (answerIndex === quizQuestions[currentQuestion].correctAnswer) {
      setQuizScore(quizScore + 1);
    }
  };\n\n`;

    code += `  const handleNextQuestion = () => {
    if (currentQuestion < quizQuestions.length - 1) {
      setCurrentQuestion(currentQuestion + 1);
      setSelectedAnswer(null);
    } else {
      setQuizComplete(true);
    }
  };\n\n`;
  }

  if (activities.some((a) => a.type === 'reflection')) {
    code += `  const handleSubmitReflection = async () => {
    if (reflectionResponse.trim().length < ${
      activities.find((a) => a.type === 'reflection')?.props.minResponseLength || 100
    }) {
      setFeedback('Please provide a more detailed reflection (at least ${
        activities.find((a) => a.type === 'reflection')?.props.minResponseLength || 100
      } words).');
      setShowFeedback(true);
      setNeedsRetry(true);
      return;
    }

    if (isNonsensical(reflectionResponse)) {
      setFeedback('Your response needs more depth and thoughtfulness. Please try again.');
      setShowFeedback(true);
      setNeedsRetry(true);
      return;
    }

    setIsGeneratingFeedback(true);
    try {
      const aiResponse = await generateEducationFeedback(
        reflectionResponse,
        '${activities.find((a) => a.type === 'reflection')?.props.prompt || 'Reflect on what you learned.'}'
      );

      setFeedback(aiResponse);
      setShowFeedback(true);

      const feedbackIndicatesRetry =
        aiResponse.toLowerCase().includes('does not address') ||
        aiResponse.toLowerCase().includes('please re-read') ||
        aiResponse.toLowerCase().includes('inappropriate language') ||
        aiResponse.toLowerCase().includes('off-topic') ||
        aiResponse.toLowerCase().includes('must elaborate');

      if (feedbackIndicatesRetry) {
        const newAttemptCount = attemptCount + 1;
        setAttemptCount(newAttemptCount);
        if (newAttemptCount >= MAX_ATTEMPTS) {
          setShowEscapeHatch(true);
        }
      }

      setNeedsRetry(feedbackIndicatesRetry);
    } catch (error) {
      console.error('Feedback generation error:', error);
      setFeedback('Unable to generate feedback. Please try again.');
      setNeedsRetry(true);
    } finally {
      setIsGeneratingFeedback(false);
    }
  };\n\n`;

    code += `  const handleTryAgain = () => {
    setReflectionResponse('');
    setFeedback('');
    setShowFeedback(false);
    setNeedsRetry(false);
    // DO NOT reset attemptCount or showEscapeHatch - they persist across retries
  };\n\n`;

    code += `  const handleContinueAnyway = () => {
    console.log('Student bypassed validation after 2 attempts');
    handleActivityComplete();
  };\n\n`;
  }

  if (activities.some((a) => a.type === 'scenario')) {
    code += `  const handleScenarioSubmit = () => {
    if (scenarioResponse.trim().length >= 100) {
      handleActivityComplete();
    }
  };\n\n`;
  }

  if (activities.some((a) => a.type === 'exit-ticket')) {
    code += `  const handleExitTicketSubmit = () => {
    if (exitTicketResponse.trim().length >= 100) {
      handleActivityComplete();
    }
  };\n\n`;
  }

  return code;
}

/**
 * Generate standard handlers (progress, completion, etc.)
 */
function generateStandardHandlers(): string {
  return `  const handleActivityComplete = () => {
    const updated = [...activities];
    updated[currentActivity].completed = true;
    setActivities(updated);

    if (currentActivity < activities.length - 1) {
      setCurrentActivity(currentActivity + 1);
    } else {
      setCurrentActivity(activities.length); // Move to certificate
    }
  };

  const handleCertificateDownload = () => {
    clearProgress(MODULE_ID);
  };

  const handleResumeProgress = () => {
    const progress = loadProgress(MODULE_ID, activities);
    if (progress) {
      setCurrentActivity(progress.currentActivity);
      setActivities(progress.activities);
    }
    setShowResumeDialog(false);
  };

  const handleStartFresh = () => {
    clearProgress(MODULE_ID);
    setShowResumeDialog(false);
  };`;
}

/**
 * Generate Progress Persistence logic
 */
function generateProgressPersistenceLogic(): string {
  return `  // Load progress on mount
  useEffect(() => {
    const progress = loadProgress(MODULE_ID, activities);
    if (progress) {
      setSavedProgress(getProgressSummary(MODULE_ID));
      setShowResumeDialog(true);
    }
  }, []);

  // Register activities with Dev Mode (ONCE on mount)
  useEffect(() => {
    clearRegistry();
    activities.forEach((activity, index) => {
      registerActivity({
        id: activity.id,
        title: activity.title,
        type: 'activity',
        moduleId: MODULE_ID,
        index,
      });
    });
  }, []); // Empty deps - only register once

  // Auto-save progress
  useEffect(() => {
    if (!showResumeDialog && currentActivity > 0) {
      saveProgress(MODULE_ID, currentActivity, activities, MODULE_VERSION);
    }
  }, [currentActivity, activities, showResumeDialog]);`;
}

/**
 * Generate Dev Mode integration logic
 */
function generateDevModeIntegration(): string {
  return `  // Listen for Dev Mode navigation
  useEffect(() => {
    const handleGoToActivity = (event: CustomEvent) => {
      setCurrentActivity(event.detail);
    };
    window.addEventListener('goToActivity', handleGoToActivity as EventListener);
    return () => window.removeEventListener('goToActivity', handleGoToActivity as EventListener);
  }, []);`;
}

/**
 * Generate activity rendering code
 */
function generateActivityRendering(activities: ActivityConfig[]): string {
  let code = '';

  activities.forEach((activity, index) => {
    switch (activity.type) {
      case 'video':
        code += generateVideoRendering(activity, index);
        break;
      case 'quiz':
        code += generateQuizRendering(activity, index);
        break;
      case 'reflection':
        code += generateReflectionRendering(activity, index);
        break;
      case 'scenario':
        code += generateScenarioRendering(activity, index);
        break;
      case 'exit-ticket':
        code += generateExitTicketRendering(activity, index);
        break;
      case 'interactive':
        code += generateInteractiveRendering(activity, index);
        break;
      default:
        code += generatePlaceholderRendering(activity, index);
    }
    code += '\n\n';
  });

  return code;
}

