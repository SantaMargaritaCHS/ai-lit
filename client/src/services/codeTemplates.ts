export const MODULE_CONTAINER_TEMPLATE = `import React, { useState, useEffect } from 'react';
import { X } from 'lucide-react';
import { Button } from '@/components/ui/button';
import { useActivityRegistry } from '@/context/ActivityRegistryContext';
import { saveProgress, loadProgress, clearProgress, getProgressSummary } from '@/lib/progressPersistence';
import ResumeProgressDialog from '@/components/shared/ResumeProgressDialog';
import CertificateActivity from '@/components/shared/CertificateActivity';

/**
 * {{MODULE_NAME}} - {{MODULE_DESCRIPTION}}
 *
 * **Generated by Module Builder** - {{GENERATED_DATE}}
 *
 * Estimated Time: {{ESTIMATED_TIME}}
 * Target Audience: {{TARGET_AUDIENCE}}
 *
 * Features:
 * - Self-contained module with {{ACTIVITY_COUNT}} activities
 * - Progress persistence (resume on refresh)
 * - Developer Mode integration (Ctrl+Alt+D navigation)
 * - Certificate generation on completion
 * - Accessibility compliant (WCAG 2.1 AA)
 *
 * Module Structure:
 {{ACTIVITY_LIST}}
 */

interface {{MODULE_NAME}}Props {
  userName: string;
  onClose?: () => void;
}

const MODULE_ID = '{{MODULE_ID}}';
const MODULE_VERSION = '1.0.0';

export default function {{MODULE_NAME}}({ userName, onClose }: {{MODULE_NAME}}Props) {
  const [currentActivity, setCurrentActivity] = useState(0);
  const [activities, setActivities] = useState<{ id: string; title: string; completed: boolean }[]>([
    {{ACTIVITY_DEFINITIONS}}
  ]);

  const [savedProgress, setSavedProgress] = useState<any>(null);
  const [showResumeDialog, setShowResumeDialog] = useState(false);

  const { registerActivity, clearRegistry } = useActivityRegistry();

  // Load progress on mount
  useEffect(() => {
    const progress = loadProgress(MODULE_ID, activities);
    if (progress) {
      setSavedProgress(getProgressSummary(MODULE_ID));
      setShowResumeDialog(true);
    }
  }, []);

  // Register activities with Dev Mode (ONCE on mount)
  useEffect(() => {
    clearRegistry();
    activities.forEach((activity, index) => {
      registerActivity({
        id: activity.id,
        title: activity.title,
        type: 'activity', // Generic type
        moduleId: MODULE_ID,
        index,
      });
    });
  }, []); // Empty deps - only register once

  // Listen for Dev Mode navigation
  useEffect(() => {
    const handleGoToActivity = (event: CustomEvent) => {
      setCurrentActivity(event.detail);
    };
    window.addEventListener('goToActivity', handleGoToActivity as EventListener);
    return () => window.removeEventListener('goToActivity', handleGoToActivity as EventListener);
  }, []);

  // Auto-save progress
  useEffect(() => {
    if (!showResumeDialog && currentActivity > 0) {
      saveProgress(MODULE_ID, currentActivity, activities, MODULE_VERSION);
    }
  }, [currentActivity, activities, showResumeDialog]);

  const handleResumeProgress = () => {
    const progress = loadProgress(MODULE_ID, activities);
    if (progress) {
      setCurrentActivity(progress.currentActivity);
      setActivities(progress.activities);
    }
    setShowResumeDialog(false);
  };

  const handleStartFresh = () => {
    clearProgress(MODULE_ID);
    setShowResumeDialog(false);
  };

  const handleActivityComplete = () => {
    const updated = [...activities];
    updated[currentActivity].completed = true;
    setActivities(updated);

    if (currentActivity < activities.length - 1) {
      setCurrentActivity(currentActivity + 1);
    } else {
      // All activities complete, move to certificate
      setCurrentActivity(activities.length);
    }
  };

  const handleCertificateDownload = () => {
    clearProgress(MODULE_ID);
  };

  return (
    <>
      {showResumeDialog && savedProgress && (
        <ResumeProgressDialog
          savedProgress={savedProgress}
          onResume={handleResumeProgress}
          onStartFresh={handleStartFresh}
        />
      )}

      <div className="min-h-screen bg-gradient-to-br from-purple-50 to-blue-100">
        {/* Header */}
        <header className="bg-white shadow-sm border-b border-gray-200">
          <div className="container mx-auto px-4 py-4 flex items-center justify-between">
            <div>
              <h1 className="text-2xl font-bold text-gray-900">{{MODULE_TITLE}}</h1>
              <p className="text-sm text-gray-600">{{MODULE_DESCRIPTION}}</p>
            </div>
            {onClose && (
              <button
                onClick={onClose}
                className="p-2 text-gray-600 hover:text-gray-900 hover:bg-gray-100 rounded-lg transition-colors"
                aria-label="Close module"
              >
                <X className="w-5 h-5" />
              </button>
            )}
          </div>
        </header>

        {/* Main Content */}
        <main className="container mx-auto px-4 py-8">
          {{ACTIVITY_RENDERING}}

          {/* Certificate */}
          {currentActivity === activities.length && (
            <CertificateActivity
              userName={userName}
              moduleName="{{MODULE_TITLE}}"
              completionDate={new Date().toLocaleDateString()}
              onDownload={handleCertificateDownload}
            />
          )}
        </main>
      </div>
    </>
  );
}
`;

