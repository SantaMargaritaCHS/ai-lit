/**
 * Helper: Generate TypeScript imports
 */
export function generateImports(activityTypes: string[]): string {
  const imports = new Set<string>([
    "import React, { useState, useEffect } from 'react';",
    "import { X } from 'lucide-react';",
    "import { Button } from '@/components/ui/button';",
    "import { useActivityRegistry } from '@/context/ActivityRegistryContext';",
    "import { saveProgress, loadProgress, clearProgress, getProgressSummary } from '@/lib/progressPersistence';",
    "import ResumeProgressDialog from '@/components/shared/ResumeProgressDialog';",
    "import CertificateActivity from '@/components/shared/CertificateActivity';",
  ]);

  if (activityTypes.includes('video')) {
    imports.add("import PremiumVideoPlayer from '@/components/shared/PremiumVideoPlayer';");
  }

  if (activityTypes.includes('reflection')) {
    imports.add("import { generateEducationFeedback, isNonsensical } from '@/utils/aiEducationFeedback';");
  }

  return Array.from(imports).join('\n');
}

