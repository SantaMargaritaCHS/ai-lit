/**
 * codeTemplates.ts - Code generation templates for Module Builder
 *
 * Phase 3.1 of Module Builder
 *
 * Purpose:
 * Transforms JSON module definitions into production-ready TypeScript/React code
 * that follows all existing platform patterns and standards.
 *
 * Template Requirements:
 * - TypeScript interfaces for all props
 * - Accessibility compliance (WCAG 2.1 AA)
 * - Dev Mode integration
 * - Progress Persistence integration
 * - AI Validation integration (for reflection activities)
 * - Self-contained module pattern
 * - Certificate generation
 *
 * Template Variables (use double curly braces):
 * - {{MODULE_NAME}} - PascalCase module name
 * - {{MODULE_ID}} - kebab-case module ID
 * - {{MODULE_TITLE}} - Display title
 * - {{MODULE_DESCRIPTION}} - Description
 * - {{ESTIMATED_TIME}} - e.g., "20 min"
 * - {{USER_NAME}} - userName prop (passed from parent)
 * - {{ACTIVITIES}} - JSON array of activity configs
 * - {{VIDEO_URL}} - Video URL for video activities
 * - {{VIDEO_SEGMENTS}} - JSON array of video segments
 *
 * Usage:
 * ```typescript
 * const moduleCode = generateModuleCode(moduleDefinition);
 * const activityCode = generateActivityCode(activityDefinition);
 * ```
 */

